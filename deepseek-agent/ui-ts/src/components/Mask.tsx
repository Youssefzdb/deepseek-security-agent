// Color each row based on its visual importance
// Row 0 = top arc (gold), Rows 1-3 = forehead (white),
// Rows 4-5 = eyes (bright white + gold pupils),
// Rows 6-8 = nose (dim), Rows 9-11 = mouth/beard (white), bottom = dim
const ROW_COLORS = [
  "#c8aa50", // 0  top arc — gold
  "#c8c8b8", // 1  forehead
  "#c8c8b8", // 2
  "#ffffff", // 3  brow — bright
  "#56c8d8", // 4  eye line — teal
  "#56c8d8", // 5
  "#c8c8b8", // 6  nose
  "#c8c8b8", // 7
  "#c8c8b8", // 8
  "#ffffff", // 9  mouth — bright
  "#c8c8b8", // 10
  "#c8aa50", // 11 chin accent — gold
  "#c8c8b8", // 12
  "#888878", // 13 lower — dim
  "#888878", // 14
  "#585858", // 15 bottom — very dim
];

interface MaskProps {
  animate?: boolean;
  fps?: number;
}

export const Mask: React.FC<MaskProps> = ({ animate = true, fps = 5 }) => {
  const [frame, setFrame] = useState(0);

  useEffect(() => {
    if (!animate) return;
    const id = setInterval(() => {
      setFrame(f => (f + 1) % FRAMES.length);
    }, 1000 / fps);
    return () => clearInterval(id);
  }, [animate, fps]);

  const lines = FRAMES[frame];

  return (
    <Box flexDirection="column">
      {lines.map((line, i) => (
        <Text
          key={i}
          color={ROW_COLORS[i] ?? "#585858"}
          bold={i <= 3 || i === 9}
        >
          {line}
        </Text>
      ))}
    </Box>
  );
};
